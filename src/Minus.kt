class Minus {
    fun print(nil1: Int, nil2: Int) {
        println("+ Fitur Pengurangan +")
        println("=====================")
        println("Nilai A : ${nil1}")
        println("Nilai B : ${nil2}")
        println("\nTotal Pengurangan : ${nil1.minus(nil2)}")
        println("=====================")
    }
}