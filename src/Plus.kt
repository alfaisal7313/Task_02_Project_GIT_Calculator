class Plus {
    fun print(nil1: Int, nil2: Int) {
        println("=====================")
        println("Nilai A : ${nil1}")
        println("Nilai B : ${nil2}")
        println("=====================")
        println("Fitur Penambahan\n")
        println("Total Penambahan : ${nil1 + nil2}")
        println("=====================")
    }
}